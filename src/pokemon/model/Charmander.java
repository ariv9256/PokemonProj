package pokemon.model;

public class Charmander 
{
	public Charmander()
	{
		super(4, "Fire boy");
	}
	public Charmander(int flamethrower, void inferno)
	{
		super(flamethrower, inferno);
	}
	public int flamethrower()
	{
		return flamethrower();
	}
	public void inferno()
	{
		return inferno();
	}

}
